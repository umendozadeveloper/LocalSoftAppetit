function llenaTabla(muestra) {
  for(f=0; f<totalFilas; f++) {
    for(c=1; c<misColumnas; c++) {
      laTabla.querySelectorAll("tr")[f].querySelectorAll("td")[c].innerHTML = elContenido[f][+muestra+c]; 
    }
  }
