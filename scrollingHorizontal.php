    }
  }


  var nuevaTabla = ""; 
  
  for(r=0; r<totalFilas; r++) {
    nuevaTabla += "<tr>"; 
  
    for(d=0; d<misColumnas; d++) {
      nuevaTabla += "<td></td>";
    }
  
    nuevaTabla += "</tr>"; 
  }
  
  laTabla.innerHTML = nuevaTabla; 
  
    for(t=0; t<totalFilas; t++) {
      laTabla.querySelectorAll("tr")[t].querySelector("td").innerHTML = elContenido[t][0]; 
    }
  
  llenaTabla(0); 
  
